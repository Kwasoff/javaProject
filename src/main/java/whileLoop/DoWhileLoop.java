package whileLoop;

public class DoWhileLoop {
    public static void main(String[] args) {

        int a = 1;

        do {
            System.out.println(a);
            a++;
        } while (a <= 10);
//========================================================================================
        int b = 1;

        do {
            if (b % 2 != 0) {
                System.out.println(b);
            }
            b++;
        }

        while (b <= 100);












    }

}