package forLoop;

public class HomeworkForLoop {
    public static void main(String[] args) {

        for (int a = 1; a <= 50; a++){
            if (a % 5 == 0){
                System.out.println(a);
            }
        }

        //=======================================================================================
        System.out.println();
        //=======================================================================================

        for (int b = 2; b <= 20; b += 2){
            System.out.println(b);
        }
        //====================================================================================
        System.out.println();
        //==================================================================================

        for (int c = 10; c >= 1; c--){
            System.out.println(c);
        }
        //========================================================================================
        System.out.println();
        //=================================================================================

        for (int d = 1; d <= 5; d++){
            for (int e = 1; e <= 5; e++){
                System.out.print(d * e + " \t");
            }
            System.out.println();
        }
        //==================================================================================
        System.out.println();
        //=================================================================================

        for (int f = 1; f <= 40; f++) {
            if (f % 2 != 0) {
                System.out.println(f);
            }
        }
        //==============================================================================
        System.out.println();
        //=============================================================================

        int sum = 0;
        for (int g = 1; g <= 100; g++){
            if (g % 2 == 0){
                System.out.println(g);
                sum = sum + g;
            }

        }
        System.out.println("The sum is: " + sum);

        //==============================================================================
        System.out.println();
        //============================================================================

       int sum1 = 0;

        for (int j = 12345; j > 0; j /= 10){
            sum1 += j % 10;
        }
        System.out.println(sum1);

        //========================================================================
        System.out.println();
        //=======================================================================

        for (int k = 5; k >= 1; k--){
            for (int l = 1; l <= k; l++){
                System.out.print(" * " + " \t");
            }
            System.out.println();
        }
        //====================================================================
        System.out.println();
        //===================================================================

        for (int x = 0; x <= 4; x++){
            for (int y = 1; y <= x; y++){
                System.out.print(x + " \t");
            }
            System.out.println();
        }





    }
}
