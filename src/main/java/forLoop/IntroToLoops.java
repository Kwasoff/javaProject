package forLoop;

public class IntroToLoops {
    public static void main(String[] args) {

        int q;
        for (q = 0; q <= 100; q++){
            System.out.println(q);
        }

        int a;
        for (a = 1; a <= 20; a++){
            System.out.println(a + " apples");
        }

        int z;
        int x;
        for (z = 2, x = 1; x <= 10; x++){
            System.out.println(z + " * " + x + " = " + z * x);
        }
        System.out.println();

        String d = "Java is fun";
        for (int c = 1; c <= 5; c++){
            System.out.println(d);
        }
        System.out.println();













    }
}
